// Fixed start date so the rotation is stable and identical for every user
// on a given calendar day. Change this only if you want to "reset" the cycle.
const EPOCH = Date.UTC(2026, 6, 15); // July 15, 2026

export function verseRefForDate(date = new Date()) {
  const dayUTC = Date.UTC(date.getFullYear(), date.getMonth(), date.getDate());
  const diffDays = Math.floor((dayUTC - EPOCH) / 86400000);
  const idx =
    ((diffDays % MEMORY_VERSES.length) + MEMORY_VERSES.length) %
    MEMORY_VERSES.length;
  return MEMORY_VERSES[idx];
}
